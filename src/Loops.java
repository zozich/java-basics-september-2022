public class Loops {
  public static void main(String[] args) {
    // for
    for (int i = 0; i < 100; i = i + 2) {
      System.out.println(i);
    }

//    for (int i = 0; i < 50; i++) {
//
//    }

    // while
    int i = 0;
    while (i < 100) {
      System.out.println(i);
      i = i + 2;
    }
  }
}
